import { useState, forwardRef, useImperativeHandle } from 'react'
import PropTypes from 'prop-types'

const Togglable = forwardRef((props, refs) => {
  const [visible, setVisible] = useState(false)

  const hideVis = { display: visible ? 'none' : '' }
  const showVis = { display: visible ? '' : 'none' }

  const toggleVisibility = () => {
    setVisible(!visible)
  }

  useImperativeHandle(refs, () => {
    return {
      toggleVisibility,
    }
  })

  return (
    <div>
      <div style={hideVis}>
        <button onClick={toggleVisibility} id='view'>{props.buttonLabel}</button>
      </div>
      <div style={showVis} className='togglableContent'>
        {props.children}
        <button onClick={toggleVisibility} id='hide'>hide</button>
      </div>
    </div>
  )
})

Togglable.displayName = 'Togglable'

Togglable.propTypes = {
  buttonLabel: PropTypes.string.isRequired,
}

export default Togglable
